//Declares the caculator class
public class Calculator {
	//declares the add method and returns the answer for the two given numbers
	public static int add(int first_number, int second_number)
	{
		return first_number + second_number;
	}
	//declares the subtract method and returns the answer for the two given numbers
	public static int subtract(int first_number, int second_number)
	{
		return first_number - second_number;
	}
	//declares the multiply method and returns the answer for the two given numbers
	public static int multiply(int first_number, int second_number)
	{
		return first_number * second_number;
	}
	//declares the divide method and returns the answer for the two given numbers
	public static double divide(int first_number, int second_number)
	{
		return first_number / second_number;
	}
	//declares the square method and returns the answer for the given number
	public static int square(int number)
	{
		return number * number;
	}
}
